public class Calculator {
    public int summe(int zahl1, int zahl2){
        return zahl1 + zahl2;
    }

    public int subtraktion(int zahl1, int zahl2){
        return zahl1 - zahl2;
    }

    public double division(int zahl1, int zahl2){
        return zahl1/zahl2;
    }

    public int multiplikation(int zahl1, int zahl2){
        return zahl1*zahl2;
    }
}


